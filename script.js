function buatTombolPelajaran() {
  const container = document.getElementById('pelajaranList');
  container.innerHTML = '';
  for (const key in materiData) {
    const btn = document.createElement('button');
    btn.textContent = materiData[key].judul;
    btn.onclick = () => showMateri(key);
    container.appendChild(btn);
  }
}

function showMateri(pelajaran) {
  const data = materiData[pelajaran];
  document.getElementById('judulMateri').innerHTML = data.judul;
  document.getElementById('isiMateri').innerHTML = data.isi;
  document.getElementById('pelajaranList').style.display = 'none';
  document.getElementById('materi').style.display = 'block';
}

function kembali() {
  document.getElementById('materi').style.display = 'none';
  document.getElementById('pelajaranList').style.display = 'block';
}

document.addEventListener('DOMContentLoaded', buatTombolPelajaran);
